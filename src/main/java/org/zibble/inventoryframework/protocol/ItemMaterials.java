    private static ItemMaterial define(ItemType type) {
        ItemMaterial material = new ItemMaterial(type);
        BY_NAME.put(type.getName().getKey(), material);
        return material;
    }

    private static ItemMaterial define(int data, ItemType legacy, ItemType modern) {
        ItemMaterial material;
        if (IS_LEGACY) {
            material = new ItemMaterial((byte) data, legacy);
        } else {
            material = new ItemMaterial(modern);
        }

        BY_NAME.put(modern.getName().getKey(), material);

        return material;
    }

    public static Optional<ItemMaterial> getByName(String material) {
        String sub = material.toLowerCase().trim().replace("minecraft:", "");
        String[] split = sub.split(":");
        ItemMaterial result = BY_NAME.get(split[0]);
        if (result == null) {
            for (ItemMaterial value : BY_NAME.values()) {
                if (value.asProtocol().getName().getKey().equalsIgnoreCase(split[0])) {
                    if (split.length > 1) {
                        if (value.legacyData() == Byte.parseByte(split[1])) {
                            return Optional.of(value);
                        }
                    } else {
                        result = value;
                        break;
                    }
                }
            }
        }

        return Optional.ofNullable(result);
    }

    public static Optional<ItemMaterial> getById(int id) {
        return getById(id, (byte) 0);
    }

    public static Optional<ItemMaterial> getById(int id, byte data) {
        for (ItemMaterial value : BY_NAME.values()) {
            if (value.asProtocol().getId() == id && value.legacyData() == data) {
                return Optional.of(value);
            }
        }
        return Optional.empty();
    }

    private static boolean supports(ServerVersion version) {
        return InventoryFramework.framework().getServerVersion().isNewerThan(version);
    }

